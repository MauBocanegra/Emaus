package periferico.emaus.domainlayer.objetos;

public class LiquidaCon {

    int tasasup;
    int tasainf;
    float bon;

    public LiquidaCon() {}

    public int getTasasup() {
        return tasasup;
    }

    public void setTasasup(int tasasup) {
        this.tasasup = tasasup;
    }

    public int getTasainf() {
        return tasainf;
    }

    public void setTasainf(int tasainf) {
        this.tasainf = tasainf;
    }

    public float getBon() {
        return bon;
    }

    public void setBon(float bon) {
        this.bon = bon;
    }
}
